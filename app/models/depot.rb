  def aggregated_places
    neighbors = self.places
    neighbors.each do |neighbor|
      if neighbor.subtype == "Farm"
        neighbor.places.each do |potential_depot|
          if potential_depot.is_a?(Depot)
            neighbors << potential_depot.place
          end
        end
      end
    end
    neighbors.uniq
  end

